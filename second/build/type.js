// numbers

let number = [1, 2, 3];
console.log(typeof number); // output : number

// strings

let string = "salar";
console.log(typeof string); // output : string

// booleans

let x = true;
console.log(typeof x); // output : boolean

// objects

let webSite = { name: "quera", age: 8, isActive: true };
console.log(typeof webSite); // output : object

//symbols

let id = Symbol("id");
console.log(typeof id); // output : symbol
